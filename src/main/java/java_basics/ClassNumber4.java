package java_basics;


public class ClassNumber4 {
    public static void main(String[] args) {
        //For Loop
        for ( int i = 0;i < 10; i++) {
            System.out.println(i);
        }

        for ( int j = 20;j > 10; j--) {
            System.out.println(j);
        }

        boolean iAmAGirl= false;
        System.out.println(iAmAGirl);
        System.out.println(!iAmAGirl);
    }


}
